const densifyPath = (path, maxStep = 0.00085) =>
  path.flatMap((point, index) => {
    if (index === path.length - 1) {
      return [point]
    }

    const nextPoint = path[index + 1]
    const distance = Math.hypot(nextPoint[0] - point[0], nextPoint[1] - point[1])
    const steps = Math.max(1, Math.ceil(distance / maxStep))

    return Array.from({ length: steps }, (_, stepIndex) => [
      point[0] + ((nextPoint[0] - point[0]) * stepIndex / steps),
      point[1] + ((nextPoint[1] - point[1]) * stepIndex / steps)
    ])
  })

